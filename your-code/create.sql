CREATE TABLE CARS (
	ID INT PRIMARY KEY,
    Vin VARCHAR(20),
    Manufacturer VARCHAR(20),
    Model VARCHAR(20),
    Year YEAR,
    Color VARCHAR(20)
);
CREATE TABLE CUSTOMERS (
    ID NUMERIC,
    Customer_id INT PRIMARY KEY AUTO_INCREMENT,
    Name VARCHAR(40) NOT NULL,
    Phone VARCHAR(20),
    Email VARCHAR(40),
    Address VARCHAR(40),
    City VARCHAR(40),
    State_Province VARCHAR(40),
    Country VARCHAR(40),
    ZIP NUMERIC
);

ALTER TABLE CUSTOMERS AUTO_INCREMENT = 10001;

